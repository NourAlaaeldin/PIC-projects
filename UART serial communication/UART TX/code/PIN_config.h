#define PIN3            0x08 /**< a preprocessor to define pin3 */
#define PIN4            0x10 /**< a preprocessor to define pin4 */
#define PIN5            0x20 /**< a preprocessor to define pin5 */
#define PIN6            0x40 /**< a preprocessor to define pin6 */
#define PIN7            0x80 /**< a preprocessor to define pin7 */

